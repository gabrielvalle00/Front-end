console.log('-----------01-------------');
//O que são operadores em Javascript
//São simbolos especiais que envolvem um ou mais operandos com a finalidade produzir um determinado resultado

//OPERADOR DE ATRIBUIÇÃO
// O operador de atribuição básico é o sinal de igual(=), ele atribui um valor a uma variavel:
var x; //declaração de variável x

x = 5; // Essa operção atribui a X o valor 5
console.log(x);

console.log('-----------02-------------');
//OPERADOR COM ATRIBUIÇÃO COMPOSTA COM ADIÇÃO (+=)

x += 10;
console.log(x);

console.log('-----------03-------------');
//OPERADOR COM ATRIBUIÇÃO COMPOSTA COM SUBTRAÇÃO (-=)

x -= 2;
console.log(x);

console.log('-----------04-------------');
//OPERADOR COM ATRIBUIÇÃO COMPOSTA COM MULTIPLICAÇÃO (*=)

x *= 2;
console.log(x);

console.log('-----------05-------------');
//OPERADOR COM ATRIBUIÇÃO COMPOSTA COM DIVISÃO (/=)

x /= 3;
console.log(x);

console.log('-----------06-------------');
//OPERADOR COM ATRIBUIÇÃO COMPOSTA COM RESTO (%=)

x %= 2;
console.log(x);
